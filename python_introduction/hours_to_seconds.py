

# =========================================================================================
# Let's say we have 2 hours that we want to convert to seconds
hours = 2
# =========================================================================================

# =========================================================================================
# There are 3600 seconds in 1 hour, so multiply hours by 3600
seconds = hours * 3600
# =========================================================================================

# =========================================================================================
# Now let's print how many seconds it is
print(f"{hours} hour(s) is {seconds} seconds.")
