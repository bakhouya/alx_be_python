
# =========================================================================================
# get the user's current age as input and convert it from string to integer
current_age = int(input("How old are you? "))
# =========================================================================================

# =========================================================================================
# calculate the number of years between 2023 and 2050
years_until_2050 = 2025 - 2023  
# =========================================================================================


# =========================================================================================
# add the user's current age in 2023 (e.g., 30) to the number of years until 2050
age_in_2050 = current_age + years_until_2050
# =========================================================================================


# =========================================================================================
# print the result so the user can see how old they will be in 2050
print(f"In 2050, you will be {age_in_2050} years old.")
