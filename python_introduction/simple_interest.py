

# =========================================================================================
# principal is the amount of money we are investing, here we set it to 1000 dollars
principal = 1000

# rate is the yearly interest rate, 5% written as 0.05
rate = 0.05

# time is how long the money will be invested, in years. Here it's 3 years
time = 3
# =========================================================================================

# =========================================================================================
# now we calculate the simple interest using the formula: principal × rate × time
interest = principal * rate * time
# =========================================================================================

# =========================================================================================
# finally, we print out the result so we can see it
print(f"The simple interest is: {interest}")
# =========================================================================================